public class sum_methods {
    static int num(int x,int y){
        int z=x+y;
        return z;
    }
    public static void main(String[] args) {
        int a=99;
        int b=22;
        int c=num(a, b);
        int a1=100;
        int b1=200;
        int c1=num(a1,b1);
        System.out.println(c);
        System.out.println(c1);

    }
}
